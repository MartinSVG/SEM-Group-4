package com.napier.semgroup4;

/**
 * Represents a city
 */
public class City {
    /**
     * City Name
     */
    public String Name;

    /**
     * City Country Code
     */
    public String CountryCode;

    /**
     * City District
     */
    public String District;

    /**
     * City Population
     */
    public int Population;

    /**
     * City Continent
     */
    public String Continent;

    /**
     * City Region
     */
    public String Region;

    /**
     * City Country
     */
    public String Country;

}
